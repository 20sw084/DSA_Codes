public class Main
{
	public static void main(String[] args) {
	    int n;
	    int square=0;
	    Scanner sc=new Scanner(System.in);
	    System.out.println("Enter Number to find Square");
	    n=sc.nextInt();
	    for(int i=0;i<n;i++){
	        square+=n;
	    }
	    System.out.println("Square is "+square);
	}
}




















